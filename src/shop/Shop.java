package shop;

public class Shop {
	private int id;
	private String userName;
	private String shopName;
	private String shopAddress;
	private String shopPostNum;
	private String shopNum;
	private String openHours;
	private String closedDay;
	private String shopTel;
	private String shopInformation;
	private String shopTable;
	private String userID;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getShopAddress() {
		return shopAddress;
	}
	public void setShopAddress(String shopAddress) {
		this.shopAddress = shopAddress;
	}
	public String getShopPostNum() {
		return shopPostNum;
	}
	public void setShopPostNum(String shopPostNum) {
		this.shopPostNum = shopPostNum;
	}
	public String getShopNum() {
		return shopNum;
	}
	public void setShopNum(String shopNum) {
		this.shopNum = shopNum;
	}
	public String getOpenHours() {
		return openHours;
	}
	public void setOpenHours(String openHours) {
		this.openHours = openHours;
	}
	public String getClosedDay() {
		return closedDay;
	}
	public void setClosedDay(String closedDay) {
		this.closedDay = closedDay;
	}
	public String getShopTel() {
		return shopTel;
	}
	public void setShopTel(String shopTel) {
		this.shopTel = shopTel;
	}
	public String getShopInformation() {
		return shopInformation;
	}
	public void setShopInformation(String shopInformation) {
		this.shopInformation = shopInformation;
	}
	public String getShopTable() {
		return shopTable;
	}
	public void setShopTable(String shopTable) {
		this.shopTable = shopTable;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	
}
